const express = require('express');

//llamamos la encriptacion de las contraseñas
const bcrypt = require('bcrypt');
//implementacion del underscore
const _ = require('underscore');
const app = express();
//implementamos el modelo usuario
const Usuario = require('../models/usuarioM');
const { verificaToken, verificaAdmin_Role, verificaUSER_ROLE, verificaTURIS_ROLE } = require('../middlewares/autenticacion');
//desifrar token
const jwt = require('jsonwebtoken');

app.get('/user',verificaToken, (req,res)=>{
    let token = req.get('token');

    jwt.verify(token, process.env.SEED, (err, decoded) => {

        if (err) {
            return res.status(401).json({
                ok: false,
                err: {
                    message: 'Token no válido'
                }
            });
        }

        res.json({
            ok: true,
            usuario: decoded.usuario
        });

    });
});
